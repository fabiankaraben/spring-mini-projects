package com.example.pdfgeneration.domain;

import jakarta.persistence.*;
import java.math.BigDecimal;

/**
 * JPA entity representing a single line item on an invoice.
 *
 * <p>Each line item has a description, quantity, unit price, and a computed
 * subtotal. Multiple line items are associated with a single {@link Invoice}
 * via a many-to-one relationship.
 *
 * <p>In the generated PDF these items are rendered as rows in a table with
 * columns: Description | Qty | Unit Price | Subtotal.
 */
@Entity
@Table(name = "invoice_line_items")
public class InvoiceLineItem {

    /**
     * Surrogate primary key, auto-generated by the database.
     */
    @Id
    @GeneratedValue(strategy = GenerationType.IDENTITY)
    private Long id;

    /**
     * The invoice this line item belongs to.
     *
     * <p>{@code @ManyToOne} with {@code FetchType.LAZY} avoids loading the full
     * invoice when only the line item is needed. {@code nullable = false} enforces
     * referential integrity at the database level.
     */
    @ManyToOne(fetch = FetchType.LAZY)
    @JoinColumn(name = "invoice_id", nullable = false)
    private Invoice invoice;

    /**
     * Human-readable description of the product or service.
     */
    @Column(name = "description", nullable = false, length = 500)
    private String description;

    /**
     * Number of units sold / hours worked, etc. Must be positive.
     */
    @Column(name = "quantity", nullable = false)
    private int quantity;

    /**
     * Price per single unit, stored as NUMERIC(12,2) to avoid floating-point issues.
     */
    @Column(name = "unit_price", nullable = false, precision = 12, scale = 2)
    private BigDecimal unitPrice;

    // ── Constructors ──────────────────────────────────────────────────────────

    /** Default constructor required by JPA. */
    protected InvoiceLineItem() {
    }

    /**
     * Creates a new line item.
     *
     * @param invoice     the parent invoice
     * @param description product/service description
     * @param quantity    number of units
     * @param unitPrice   price per unit
     */
    public InvoiceLineItem(Invoice invoice, String description, int quantity, BigDecimal unitPrice) {
        this.invoice = invoice;
        this.description = description;
        this.quantity = quantity;
        this.unitPrice = unitPrice;
    }

    // ── Business logic ────────────────────────────────────────────────────────

    /**
     * Computes the subtotal for this line item.
     *
     * <p>The subtotal is NOT stored in the database – it is always derived from
     * {@code quantity * unitPrice} to avoid inconsistencies if those fields change.
     *
     * @return {@code unitPrice} multiplied by {@code quantity}
     */
    public BigDecimal getSubtotal() {
        return unitPrice.multiply(BigDecimal.valueOf(quantity));
    }

    // ── Getters and setters ───────────────────────────────────────────────────

    public Long getId() { return id; }

    public Invoice getInvoice() { return invoice; }
    public void setInvoice(Invoice invoice) { this.invoice = invoice; }

    public String getDescription() { return description; }
    public void setDescription(String description) { this.description = description; }

    public int getQuantity() { return quantity; }
    public void setQuantity(int quantity) { this.quantity = quantity; }

    public BigDecimal getUnitPrice() { return unitPrice; }
    public void setUnitPrice(BigDecimal unitPrice) { this.unitPrice = unitPrice; }
}
