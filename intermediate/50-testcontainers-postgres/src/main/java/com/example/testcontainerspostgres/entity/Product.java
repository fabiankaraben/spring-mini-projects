package com.example.testcontainerspostgres.entity;

import jakarta.persistence.Column;
import jakarta.persistence.Entity;
import jakarta.persistence.GeneratedValue;
import jakarta.persistence.GenerationType;
import jakarta.persistence.Id;
import jakarta.persistence.Table;

import java.math.BigDecimal;

/**
 * JPA entity representing a product stored in the {@code products} table.
 *
 * <p>This is the core domain object of the mini-project. It intentionally
 * stays simple — a product has a name, description, price and a stock quantity —
 * so that the focus remains on the testing infrastructure rather than on
 * complex business logic.
 *
 * <p>Persistence notes:
 * <ul>
 *   <li>{@code @Entity} marks this class as a JPA-managed entity.</li>
 *   <li>{@code @Table(name = "products")} explicitly names the table to avoid
 *       relying on Hibernate's naming strategy.</li>
 *   <li>{@code id} is auto-generated by the database via a sequence (IDENTITY
 *       strategy maps to {@code GENERATED ALWAYS AS IDENTITY} on PostgreSQL).</li>
 *   <li>{@code price} uses {@code BigDecimal} for monetary values — never use
 *       {@code double} or {@code float} for money due to floating-point errors.</li>
 * </ul>
 */
@Entity
@Table(name = "products")
public class Product {

    /**
     * Primary key — auto-generated by the database.
     * Never set this manually; the database assigns a value on INSERT.
     */
    @Id
    @GeneratedValue(strategy = GenerationType.IDENTITY)
    private Long id;

    /**
     * The product name. Must be non-null and no longer than 255 characters.
     * {@code nullable = false} adds a NOT NULL constraint at the DB level.
     */
    @Column(nullable = false)
    private String name;

    /**
     * A human-readable description of the product. Optional field (nullable).
     * Stored as TEXT in PostgreSQL to support arbitrarily long descriptions.
     */
    @Column(columnDefinition = "TEXT")
    private String description;

    /**
     * The price of the product in the application's default currency.
     * Stored with precision 19 and scale 2 (e.g. 9999999999999999.99).
     * Must be non-null and non-negative.
     */
    @Column(nullable = false, precision = 19, scale = 2)
    private BigDecimal price;

    /**
     * The number of units currently in stock.
     * Must be non-null and non-negative.
     */
    @Column(nullable = false)
    private Integer stockQuantity;

    /**
     * Protected no-arg constructor required by the JPA specification.
     * Hibernate needs to instantiate entities via reflection when loading
     * them from the database. This constructor must NOT be used in
     * application code — use the public constructor instead.
     */
    protected Product() {
        // Required by JPA — do not remove
    }

    /**
     * Creates a new product with all required fields.
     *
     * @param name          the product name (must not be blank)
     * @param description   an optional description (may be null)
     * @param price         the unit price (must be non-negative)
     * @param stockQuantity the initial stock quantity (must be >= 0)
     */
    public Product(String name, String description, BigDecimal price, Integer stockQuantity) {
        this.name = name;
        this.description = description;
        this.price = price;
        this.stockQuantity = stockQuantity;
    }

    // =========================================================================
    // Getters and setters
    // =========================================================================

    public Long getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public BigDecimal getPrice() {
        return price;
    }

    public void setPrice(BigDecimal price) {
        this.price = price;
    }

    public Integer getStockQuantity() {
        return stockQuantity;
    }

    public void setStockQuantity(Integer stockQuantity) {
        this.stockQuantity = stockQuantity;
    }
}
