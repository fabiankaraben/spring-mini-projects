package com.example.rabbitmqproducer.dto;

import jakarta.validation.constraints.Min;
import jakarta.validation.constraints.NotBlank;
import jakarta.validation.constraints.NotNull;

/**
 * Data Transfer Object (DTO) for the order publishing endpoint.
 *
 * <p>This class represents the JSON body that clients send to
 * {@code POST /api/messages/orders}. It acts as a validation gate:
 * Bean Validation annotations on its fields are checked by Spring MVC
 * <em>before</em> the controller method body is executed. Invalid requests
 * are rejected with HTTP 400 Bad Request and a structured error body.
 *
 * <h2>DTO vs Domain model</h2>
 * <p>Keeping the API contract (DTO) separate from the domain model
 * ({@link com.example.rabbitmqproducer.domain.OrderMessage}) provides several
 * benefits:
 * <ul>
 *   <li>The API shape can evolve independently from the internal message format.</li>
 *   <li>Sensitive internal fields (e.g. {@code messageId}, {@code createdAt})
 *       are not exposed to the caller — they are generated by the service.</li>
 *   <li>Validation rules live in the DTO and don't pollute the domain model.</li>
 * </ul>
 */
public class OrderRequest {

    /**
     * Business identifier for the order.
     *
     * <p>{@code @NotBlank} rejects null values, empty strings, and
     * whitespace-only strings. This prevents orders without a meaningful
     * identifier from entering the system.
     *
     * <p>Example: {@code "ORD-20240315-001"}
     */
    @NotBlank(message = "orderId must not be blank")
    private String orderId;

    /**
     * Name of the product to be ordered.
     *
     * <p>{@code @NotBlank} ensures the product field is always populated.
     *
     * <p>Example: {@code "Wireless Keyboard"}
     */
    @NotBlank(message = "product must not be blank")
    private String product;

    /**
     * Number of units ordered.
     *
     * <p>{@code @NotNull} catches a missing JSON field.
     * {@code @Min(1)} ensures at least one unit is ordered (ordering zero or
     * negative quantities is a business rule violation).
     */
    @NotNull(message = "quantity must not be null")
    @Min(value = 1, message = "quantity must be at least 1")
    private Integer quantity;

    // ── Constructors ──────────────────────────────────────────────────────────────

    /**
     * No-arg constructor required by Jackson for JSON deserialisation.
     */
    public OrderRequest() {
    }

    /**
     * All-args constructor used in tests to build request objects concisely.
     *
     * @param orderId  the business order identifier
     * @param product  the name of the product
     * @param quantity the number of units ordered
     */
    public OrderRequest(String orderId, String product, Integer quantity) {
        this.orderId = orderId;
        this.product = product;
        this.quantity = quantity;
    }

    // ── Getters and Setters ───────────────────────────────────────────────────────

    /**
     * @return the business order identifier
     */
    public String getOrderId() {
        return orderId;
    }

    /**
     * @param orderId the business order identifier
     */
    public void setOrderId(String orderId) {
        this.orderId = orderId;
    }

    /**
     * @return the name of the ordered product
     */
    public String getProduct() {
        return product;
    }

    /**
     * @param product the name of the ordered product
     */
    public void setProduct(String product) {
        this.product = product;
    }

    /**
     * @return the number of units ordered
     */
    public Integer getQuantity() {
        return quantity;
    }

    /**
     * @param quantity the number of units ordered
     */
    public void setQuantity(Integer quantity) {
        this.quantity = quantity;
    }
}
