package com.example.excelexport.service;

import com.example.excelexport.model.Product;
import org.apache.poi.ss.usermodel.*;
import org.apache.poi.xssf.usermodel.XSSFWorkbook;
import org.junit.jupiter.api.BeforeEach;
import org.junit.jupiter.api.DisplayName;
import org.junit.jupiter.api.Nested;
import org.junit.jupiter.api.Test;

import java.io.ByteArrayInputStream;
import java.io.IOException;
import java.math.BigDecimal;
import java.util.List;

import static org.assertj.core.api.Assertions.*;

/**
 * Unit tests for {@link ExcelExportService}.
 *
 * <p>These tests verify the Excel generation logic in isolation – no Spring
 * context is loaded, keeping execution fast. The service is instantiated
 * directly (no mocks needed because it has no external dependencies).
 *
 * <p>We read the generated bytes back into an Apache POI {@link Workbook}
 * and assert on the resulting cell values, sheet name, merged regions, etc.
 */
@DisplayName("ExcelExportService")
class ExcelExportServiceTest {

    // System under test – instantiated directly without Spring
    private ExcelExportService service;

    @BeforeEach
    void setUp() {
        service = new ExcelExportService();
    }

    // ── Helper data ─────────────────────────────────────────────────────────────

    /** Returns a minimal single-product list used by several tests. */
    private List<Product> singleProduct() {
        return List.of(
                new Product("PRD-001", "Test Laptop", "Electronics",
                        new BigDecimal("999.99"), 10)
        );
    }

    /** Returns a two-product list for multi-row tests. */
    private List<Product> twoProducts() {
        return List.of(
                new Product("PRD-001", "Laptop",  "Electronics", new BigDecimal("999.99"), 10),
                new Product("PRD-002", "Keyboard","Electronics", new BigDecimal("79.99"),   50)
        );
    }

    /**
     * Utility: generate bytes and parse them back into a POI {@link Workbook}
     * so the actual cell contents can be inspected.
     */
    private Workbook generate(String title, List<Product> products) throws IOException {
        byte[] bytes = service.generateProductReport(title, products);
        // XSSFWorkbook can be constructed directly from an InputStream
        return new XSSFWorkbook(new ByteArrayInputStream(bytes));
    }

    // ── Test groups ─────────────────────────────────────────────────────────────

    @Nested
    @DisplayName("Return value")
    class ReturnValue {

        @Test
        @DisplayName("returns a non-null, non-empty byte array")
        void returnsNonEmptyBytes() throws IOException {
            byte[] bytes = service.generateProductReport("Title", singleProduct());
            assertThat(bytes).isNotNull().isNotEmpty();
        }

        @Test
        @DisplayName("generated bytes represent a valid XLSX workbook")
        void bytesAreValidXlsx() {
            // Constructing XSSFWorkbook from the bytes would throw if the binary
            // is not a valid Office Open XML (.xlsx) file.
            assertThatCode(() -> generate("Title", singleProduct()))
                    .doesNotThrowAnyException();
        }
    }

    @Nested
    @DisplayName("Workbook structure")
    class WorkbookStructure {

        @Test
        @DisplayName("workbook contains exactly one sheet named 'Products'")
        void sheetName() throws IOException {
            try (Workbook wb = generate("My Report", singleProduct())) {
                assertThat(wb.getNumberOfSheets()).isEqualTo(1);
                assertThat(wb.getSheetAt(0).getSheetName()).isEqualTo("Products");
            }
        }

        @Test
        @DisplayName("row 0 is the title row and spans a merged region")
        void titleRowMergedRegion() throws IOException {
            try (Workbook wb = generate("Inventory Q1", singleProduct())) {
                Sheet sheet = wb.getSheetAt(0);
                // Row 0 must exist
                Row titleRow = sheet.getRow(0);
                assertThat(titleRow).isNotNull();
                // The first cell of the title row must hold the report title
                assertThat(titleRow.getCell(0).getStringCellValue())
                        .isEqualTo("Inventory Q1");
                // There must be at least one merged region covering row 0
                assertThat(sheet.getNumMergedRegions()).isGreaterThanOrEqualTo(1);
            }
        }

        @Test
        @DisplayName("row 1 contains the expected column header labels")
        void headerRow() throws IOException {
            try (Workbook wb = generate("Title", singleProduct())) {
                Row header = wb.getSheetAt(0).getRow(1);
                assertThat(header).isNotNull();
                assertThat(header.getCell(0).getStringCellValue()).isEqualTo("ID");
                assertThat(header.getCell(1).getStringCellValue()).isEqualTo("Name");
                assertThat(header.getCell(2).getStringCellValue()).isEqualTo("Category");
                assertThat(header.getCell(3).getStringCellValue()).isEqualTo("Price (USD)");
                assertThat(header.getCell(4).getStringCellValue()).isEqualTo("Stock");
            }
        }
    }

    @Nested
    @DisplayName("Data rows")
    class DataRows {

        @Test
        @DisplayName("first data row (row index 2) contains the correct product values")
        void firstDataRow() throws IOException {
            try (Workbook wb = generate("Report", singleProduct())) {
                Row dataRow = wb.getSheetAt(0).getRow(2);
                assertThat(dataRow).isNotNull();
                // String cells
                assertThat(dataRow.getCell(0).getStringCellValue()).isEqualTo("PRD-001");
                assertThat(dataRow.getCell(1).getStringCellValue()).isEqualTo("Test Laptop");
                assertThat(dataRow.getCell(2).getStringCellValue()).isEqualTo("Electronics");
                // Numeric cell – price stored as double
                assertThat(dataRow.getCell(3).getNumericCellValue()).isEqualTo(999.99);
                // Numeric cell – stock stored as double (Excel has no integer type)
                assertThat(dataRow.getCell(4).getNumericCellValue()).isEqualTo(10.0);
            }
        }

        @Test
        @DisplayName("sheet has the correct total number of rows: title + header + data + totals")
        void correctRowCount() throws IOException {
            List<Product> products = twoProducts();
            try (Workbook wb = generate("Report", products)) {
                Sheet sheet = wb.getSheetAt(0);
                // 1 title + 1 header + N data rows + 1 totals = N + 3
                int expectedRows = products.size() + 3;
                assertThat(sheet.getLastRowNum() + 1).isEqualTo(expectedRows);
            }
        }

        @Test
        @DisplayName("all products are written in sequential data rows")
        void allProductsPresent() throws IOException {
            List<Product> products = twoProducts();
            try (Workbook wb = generate("Report", products)) {
                Sheet sheet = wb.getSheetAt(0);
                // Data rows start at index 2
                for (int i = 0; i < products.size(); i++) {
                    Row row = sheet.getRow(i + 2);
                    assertThat(row).isNotNull();
                    assertThat(row.getCell(0).getStringCellValue())
                            .isEqualTo(products.get(i).id());
                }
            }
        }
    }

    @Nested
    @DisplayName("Totals row")
    class TotalsRow {

        @Test
        @DisplayName("totals row exists at index productCount + 2")
        void totalsRowExists() throws IOException {
            List<Product> products = singleProduct();
            try (Workbook wb = generate("Report", products)) {
                // 1 product → totals at row index 3 (0:title, 1:header, 2:data, 3:totals)
                Row totalsRow = wb.getSheetAt(0).getRow(3);
                assertThat(totalsRow).isNotNull();
            }
        }

        @Test
        @DisplayName("totals row label cell contains 'TOTALS'")
        void totalsRowLabel() throws IOException {
            try (Workbook wb = generate("Report", singleProduct())) {
                Row totalsRow = wb.getSheetAt(0).getRow(3);
                assertThat(totalsRow.getCell(0).getStringCellValue()).isEqualTo("TOTALS");
            }
        }

        @Test
        @DisplayName("price cell in totals row contains an AVERAGE formula")
        void totalsRowPriceFormula() throws IOException {
            try (Workbook wb = generate("Report", twoProducts())) {
                // 2 products → totals at row index 4
                Row totalsRow = wb.getSheetAt(0).getRow(4);
                Cell priceCell = totalsRow.getCell(3); // column D (index 3)
                assertThat(priceCell.getCellType()).isEqualTo(CellType.FORMULA);
                assertThat(priceCell.getCellFormula()).containsIgnoringCase("AVERAGE");
            }
        }

        @Test
        @DisplayName("stock cell in totals row contains a SUM formula")
        void totalsRowStockFormula() throws IOException {
            try (Workbook wb = generate("Report", twoProducts())) {
                Row totalsRow = wb.getSheetAt(0).getRow(4);
                Cell stockCell = totalsRow.getCell(4); // column E (index 4)
                assertThat(stockCell.getCellType()).isEqualTo(CellType.FORMULA);
                assertThat(stockCell.getCellFormula()).containsIgnoringCase("SUM");
            }
        }
    }

    @Nested
    @DisplayName("Report title")
    class ReportTitle {

        @Test
        @DisplayName("title cell reflects the exact string passed in")
        void titleMatchesInput() throws IOException {
            String customTitle = "Custom Report 2024";
            try (Workbook wb = generate(customTitle, singleProduct())) {
                String actual = wb.getSheetAt(0).getRow(0).getCell(0).getStringCellValue();
                assertThat(actual).isEqualTo(customTitle);
            }
        }

        @Test
        @DisplayName("different titles produce different title cells")
        void differentTitlesProduceDifferentCells() throws IOException {
            try (Workbook wbA = generate("Alpha", singleProduct());
                 Workbook wbB = generate("Beta",  singleProduct())) {
                String titleA = wbA.getSheetAt(0).getRow(0).getCell(0).getStringCellValue();
                String titleB = wbB.getSheetAt(0).getRow(0).getCell(0).getStringCellValue();
                assertThat(titleA).isNotEqualTo(titleB);
            }
        }
    }

    @Nested
    @DisplayName("Cell types")
    class CellTypes {

        @Test
        @DisplayName("price cell type is NUMERIC")
        void priceCellIsNumeric() throws IOException {
            try (Workbook wb = generate("T", singleProduct())) {
                Cell priceCell = wb.getSheetAt(0).getRow(2).getCell(3);
                assertThat(priceCell.getCellType()).isEqualTo(CellType.NUMERIC);
            }
        }

        @Test
        @DisplayName("stock cell type is NUMERIC")
        void stockCellIsNumeric() throws IOException {
            try (Workbook wb = generate("T", singleProduct())) {
                Cell stockCell = wb.getSheetAt(0).getRow(2).getCell(4);
                assertThat(stockCell.getCellType()).isEqualTo(CellType.NUMERIC);
            }
        }

        @Test
        @DisplayName("id, name, category cells are STRING type")
        void stringCells() throws IOException {
            try (Workbook wb = generate("T", singleProduct())) {
                Row row = wb.getSheetAt(0).getRow(2);
                assertThat(row.getCell(0).getCellType()).isEqualTo(CellType.STRING);
                assertThat(row.getCell(1).getCellType()).isEqualTo(CellType.STRING);
                assertThat(row.getCell(2).getCellType()).isEqualTo(CellType.STRING);
            }
        }
    }
}
