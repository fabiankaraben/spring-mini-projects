package com.example.mongodbcustomqueries.dto;

import java.math.BigDecimal;

/**
 * Aggregation result DTO for the "top products by revenue" query.
 *
 * <p>This class captures the output of an aggregation pipeline that:
 * <ol>
 *   <li>Unwinds the {@code items} array with {@code $unwind}.</li>
 *   <li>Groups by {@code productName}, summing revenue and quantity.</li>
 *   <li>Sorts by total revenue descending.</li>
 *   <li>Limits the results to a configurable top-N.</li>
 * </ol>
 *
 * <p>Example aggregation pipeline this DTO captures:
 * <pre>
 * [
 *   { $unwind: "$items" },
 *   { $group: {
 *       _id: "$items.product_name",
 *       totalRevenue:   { $sum: { $multiply: ["$items.unit_price", "$items.quantity"] } },
 *       totalQuantity:  { $sum: "$items.quantity" }
 *   }},
 *   { $sort: { totalRevenue: -1 } },
 *   { $limit: N }
 * ]
 * </pre>
 */
public class TopProductResult {

    /**
     * The product name (value of {@code $group._id}, which is {@code $items.product_name}).
     */
    private String productName;

    /**
     * Total revenue generated by this product across all orders.
     * Computed as {@code $sum: { $multiply: ["$items.unit_price", "$items.quantity"] }}.
     */
    private BigDecimal totalRevenue;

    /**
     * Total units sold for this product across all orders.
     * Computed as {@code $sum: "$items.quantity"}.
     */
    private Long totalQuantity;

    // ── Constructors ─────────────────────────────────────────────────────────────

    /** No-arg constructor required by Spring Data MongoDB for aggregation result mapping. */
    public TopProductResult() {}

    /**
     * Convenience constructor used in unit tests.
     *
     * @param productName    the product name
     * @param totalRevenue   total revenue for this product
     * @param totalQuantity  total units sold
     */
    public TopProductResult(String productName, BigDecimal totalRevenue, Long totalQuantity) {
        this.productName = productName;
        this.totalRevenue = totalRevenue;
        this.totalQuantity = totalQuantity;
    }

    // ── Getters and setters ───────────────────────────────────────────────────────

    public String getProductName() { return productName; }
    public void setProductName(String productName) { this.productName = productName; }

    public BigDecimal getTotalRevenue() { return totalRevenue; }
    public void setTotalRevenue(BigDecimal totalRevenue) { this.totalRevenue = totalRevenue; }

    public Long getTotalQuantity() { return totalQuantity; }
    public void setTotalQuantity(Long totalQuantity) { this.totalQuantity = totalQuantity; }

    @Override
    public String toString() {
        return "TopProductResult{productName='" + productName + "', totalRevenue=" + totalRevenue
                + ", totalQuantity=" + totalQuantity + "}";
    }
}
