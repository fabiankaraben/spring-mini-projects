package com.example.entitylifecycle.service;

import com.example.entitylifecycle.dto.ProductRequest;
import com.example.entitylifecycle.dto.ProductResponse;
import com.example.entitylifecycle.entity.Product;
import com.example.entitylifecycle.exception.ProductNotFoundException;
import com.example.entitylifecycle.repository.ProductRepository;
import org.springframework.stereotype.Service;
import org.springframework.transaction.annotation.Transactional;

import java.time.Instant;
import java.util.List;

/**
 * Service layer for {@link Product} business logic.
 *
 * <p>This class is the only place in the application where {@link Product} entities
 * are created, mutated, and deleted. The JPA lifecycle callbacks fire transparently
 * inside Hibernate — no lifecycle code appears here.
 *
 * <p>Lifecycle events that fire during the operations in this service:
 * <ul>
 *   <li><b>create</b> — {@code repository.save(product)} triggers
 *       {@code @PrePersist} (slug + timestamp generation) followed by
 *       {@code @PostPersist} (audit log entry).</li>
 *   <li><b>findById / findAll</b> — every entity loaded triggers
 *       {@code @PostLoad} (discount price calculation).</li>
 *   <li><b>update</b> — {@code repository.save(existing)} triggers
 *       {@code @PreUpdate} (audit log entry from external listener);
 *       the service manually sets {@code updatedAt} before saving so the
 *       timestamp is refreshed.</li>
 *   <li><b>delete</b> — the DELETE triggers {@code @PostRemove} (audit log entry).</li>
 * </ul>
 */
@Service
@Transactional(readOnly = true) // default: read-only transactions (no dirty checking overhead)
public class ProductService {

    private final ProductRepository productRepository;

    /**
     * Constructor injection — the recommended injection style in Spring.
     * No {@code @Autowired} annotation needed on single-constructor classes.
     *
     * @param productRepository repository for CRUD operations on {@link Product}
     */
    public ProductService(ProductRepository productRepository) {
        this.productRepository = productRepository;
    }

    /**
     * Returns all products in the database.
     *
     * <p>Each entity load triggers the {@code @PostLoad} callback, which
     * recomputes the {@code discountedPrice} transient field so the caller
     * always receives up-to-date discounted prices.
     *
     * @return list of all products as response DTOs
     */
    public List<ProductResponse> findAll() {
        return productRepository.findAll()
                .stream()
                .map(ProductResponse::from)
                .toList();
    }

    /**
     * Finds a single product by its primary key.
     *
     * @param id the product's database ID
     * @return the product as a response DTO (discountedPrice populated by @PostLoad)
     * @throws ProductNotFoundException if no product with that ID exists
     */
    public ProductResponse findById(Long id) {
        Product product = productRepository.findById(id)
                .orElseThrow(() -> new ProductNotFoundException(id));
        return ProductResponse.from(product);
    }

    /**
     * Finds a product by its URL slug.
     *
     * <p>Slugs are auto-generated by {@code @PrePersist} from the product name
     * and never change ({@code updatable = false}), making them stable permanent
     * identifiers for product URLs.
     *
     * @param slug the URL slug to look up
     * @return the product as a response DTO
     * @throws ProductNotFoundException if no product with that slug exists
     */
    public ProductResponse findBySlug(String slug) {
        Product product = productRepository.findBySlug(slug)
                .orElseThrow(() -> new ProductNotFoundException(-1L));
        return ProductResponse.from(product);
    }

    /**
     * Returns all products that have a discount greater than zero.
     *
     * <p>For each loaded entity, {@code @PostLoad} fires and recomputes the
     * {@code discountedPrice} field, so the caller always sees the correct
     * discounted price.
     *
     * @return list of discounted products as response DTOs
     */
    public List<ProductResponse> findDiscounted() {
        return productRepository.findByDiscountPercentGreaterThan(0)
                .stream()
                .map(ProductResponse::from)
                .toList();
    }

    /**
     * Creates a new product and persists it.
     *
     * <p>After {@code productRepository.save(product)} returns, the entity will
     * have its {@code slug}, {@code createdAt}, {@code updatedAt}, and
     * {@code discountedPrice} populated — all set automatically by the
     * {@code @PrePersist} callback without any manual code here.
     *
     * @param request the inbound DTO with name, description, price, and discount
     * @return the persisted product (with auto-generated ID, slug, and timestamps)
     */
    @Transactional // overrides the class-level readOnly = true
    public ProductResponse create(ProductRequest request) {
        Product product = new Product(
                request.name(),
                request.description(),
                request.price(),
                request.discountPercent()
        );
        // @PrePersist fires here: slug + timestamps + discountedPrice are set
        // @PostPersist fires after the INSERT: audit log entry is written
        Product saved = productRepository.save(product);
        return ProductResponse.from(saved);
    }

    /**
     * Updates an existing product's fields.
     *
     * <p>The {@code updatedAt} timestamp is set manually here before saving,
     * which is then logged by the external listener's {@code @PreUpdate} callback.
     * The {@code slug} is intentionally NOT updated because its column is
     * {@code updatable = false} — the original slug is preserved to keep URLs stable.
     *
     * @param id      the ID of the product to update
     * @param request the inbound DTO containing the new values
     * @return the updated product as a response DTO
     * @throws ProductNotFoundException if no product with that ID exists
     */
    @Transactional
    public ProductResponse update(Long id, ProductRequest request) {
        Product product = productRepository.findById(id)
                .orElseThrow(() -> new ProductNotFoundException(id));

        // Mutate the managed entity — Hibernate detects the changes (dirty checking)
        // and will issue an UPDATE when the transaction commits.
        product.setName(request.name());
        product.setDescription(request.description());
        product.setPrice(request.price());
        product.setDiscountPercent(request.discountPercent());

        // Refresh the updatedAt timestamp manually — the external listener's
        // @PreUpdate callback will log it for audit purposes.
        product.setUpdatedAt(Instant.now());

        Product saved = productRepository.save(product);
        return ProductResponse.from(saved);
    }

    /**
     * Deletes a product by its primary key.
     *
     * <p>After the DELETE, the external listener's {@code @PostRemove} callback
     * fires and logs the deletion event.
     *
     * @param id the ID of the product to delete
     * @throws ProductNotFoundException if no product with that ID exists
     */
    @Transactional
    public void delete(Long id) {
        if (!productRepository.existsById(id)) {
            throw new ProductNotFoundException(id);
        }
        productRepository.deleteById(id);
    }
}
