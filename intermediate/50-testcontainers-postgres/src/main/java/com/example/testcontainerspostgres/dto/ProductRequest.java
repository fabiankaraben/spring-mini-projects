package com.example.testcontainerspostgres.dto;

import jakarta.validation.constraints.DecimalMin;
import jakarta.validation.constraints.Min;
import jakarta.validation.constraints.NotBlank;
import jakarta.validation.constraints.NotNull;

import java.math.BigDecimal;

/**
 * Data Transfer Object (DTO) for incoming Product create/update requests.
 *
 * <p>Using a dedicated DTO instead of accepting the {@code Product} entity directly
 * in the controller provides several benefits:
 * <ul>
 *   <li><b>Security</b> — prevents mass-assignment attacks (clients cannot set
 *       fields like {@code id} that should only be set by the database).</li>
 *   <li><b>Validation</b> — Bean Validation annotations here are checked by
 *       Spring MVC before the request even reaches the service layer.</li>
 *   <li><b>Decoupling</b> — the HTTP API shape is independent of the database
 *       schema; either can change without breaking the other.</li>
 * </ul>
 *
 * <p>This is a Java record, which gives us an immutable object with a canonical
 * constructor, getters, {@code equals}, {@code hashCode} and {@code toString}
 * generated by the compiler — no boilerplate needed.
 *
 * @param name          the product name; must not be blank
 * @param description   an optional product description; may be null or empty
 * @param price         the unit price; must be greater than or equal to 0.00
 * @param stockQuantity the initial stock quantity; must be >= 0
 */
public record ProductRequest(

        /**
         * Product name.
         * {@code @NotBlank} rejects null, empty string and whitespace-only strings.
         */
        @NotBlank(message = "Product name must not be blank")
        String name,

        /**
         * Optional product description. No validation constraint — null is fine.
         */
        String description,

        /**
         * Product price.
         * {@code @NotNull} rejects null; {@code @DecimalMin} rejects negative prices.
         * Using "0.00" as the min (inclusive) allows free products.
         */
        @NotNull(message = "Price must not be null")
        @DecimalMin(value = "0.00", message = "Price must be zero or positive")
        BigDecimal price,

        /**
         * Stock quantity.
         * {@code @NotNull} rejects null; {@code @Min(0)} rejects negative quantities.
         */
        @NotNull(message = "Stock quantity must not be null")
        @Min(value = 0, message = "Stock quantity must be zero or positive")
        Integer stockQuantity
) {
}
