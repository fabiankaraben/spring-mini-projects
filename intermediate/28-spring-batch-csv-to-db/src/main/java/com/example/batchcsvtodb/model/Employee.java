package com.example.batchcsvtodb.model;

import jakarta.persistence.Column;
import jakarta.persistence.Entity;
import jakarta.persistence.GeneratedValue;
import jakarta.persistence.GenerationType;
import jakarta.persistence.Id;
import jakarta.persistence.Table;
import java.math.BigDecimal;
import java.time.LocalDate;

/**
 * JPA entity that represents an employee record stored in the {@code employees} table.
 *
 * <p>Each row in the CSV file maps to one {@code Employee} after passing through the
 * {@code EmployeeItemProcessor}. Only rows that pass validation are persisted.
 *
 * <p>Table schema is managed by Flyway (see {@code V1__create_employees_table.sql}).
 */
@Entity
@Table(name = "employees")
public class Employee {

    /** Surrogate primary key – auto-generated by the database sequence. */
    @Id
    @GeneratedValue(strategy = GenerationType.IDENTITY)
    private Long id;

    /**
     * First name of the employee. Maps to the {@code first_name} column.
     * Must not be blank – validated in {@code EmployeeItemProcessor}.
     */
    @Column(name = "first_name", nullable = false, length = 100)
    private String firstName;

    /**
     * Last name of the employee. Maps to the {@code last_name} column.
     * Must not be blank – validated in {@code EmployeeItemProcessor}.
     */
    @Column(name = "last_name", nullable = false, length = 100)
    private String lastName;

    /**
     * E-mail address of the employee. Must be unique in the table.
     * Validated for proper format in {@code EmployeeItemProcessor}.
     */
    @Column(name = "email", nullable = false, unique = true, length = 200)
    private String email;

    /**
     * Job title / position of the employee (e.g. "Software Engineer").
     * Optional field – may be {@code null}.
     */
    @Column(name = "department", nullable = false, length = 100)
    private String department;

    /**
     * Gross annual salary in USD. Must be a positive value.
     * Validated in {@code EmployeeItemProcessor}.
     */
    @Column(name = "salary", nullable = false, precision = 12, scale = 2)
    private BigDecimal salary;

    /**
     * Date the employee was hired. Parsed from the CSV column {@code hire_date}
     * using the pattern {@code yyyy-MM-dd}.
     */
    @Column(name = "hire_date", nullable = false)
    private LocalDate hireDate;

    // ── Constructors ──────────────────────────────────────────────────────────

    /** Required by JPA – do not use directly in application code. */
    protected Employee() {}

    /**
     * Full constructor used by the {@code EmployeeItemProcessor} to create a
     * validated entity ready for persistence.
     */
    public Employee(String firstName, String lastName, String email,
                    String department, BigDecimal salary, LocalDate hireDate) {
        this.firstName  = firstName;
        this.lastName   = lastName;
        this.email      = email;
        this.department = department;
        this.salary     = salary;
        this.hireDate   = hireDate;
    }

    // ── Getters & Setters ─────────────────────────────────────────────────────

    public Long getId() { return id; }

    public String getFirstName() { return firstName; }
    public void setFirstName(String firstName) { this.firstName = firstName; }

    public String getLastName() { return lastName; }
    public void setLastName(String lastName) { this.lastName = lastName; }

    public String getEmail() { return email; }
    public void setEmail(String email) { this.email = email; }

    public String getDepartment() { return department; }
    public void setDepartment(String department) { this.department = department; }

    public BigDecimal getSalary() { return salary; }
    public void setSalary(BigDecimal salary) { this.salary = salary; }

    public LocalDate getHireDate() { return hireDate; }
    public void setHireDate(LocalDate hireDate) { this.hireDate = hireDate; }

    @Override
    public String toString() {
        return "Employee{id=" + id +
               ", firstName='" + firstName + '\'' +
               ", lastName='" + lastName + '\'' +
               ", email='" + email + '\'' +
               ", department='" + department + '\'' +
               ", salary=" + salary +
               ", hireDate=" + hireDate + '}';
    }
}
