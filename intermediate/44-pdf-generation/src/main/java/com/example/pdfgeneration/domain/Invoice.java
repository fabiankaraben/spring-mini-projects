package com.example.pdfgeneration.domain;

import jakarta.persistence.*;
import java.math.BigDecimal;
import java.time.LocalDate;

/**
 * JPA entity representing a persisted invoice.
 *
 * <p>When a client requests an invoice PDF, the metadata (number, customer, amount,
 * date, and line items) is first saved to PostgreSQL via this entity so that a
 * report listing all generated invoices can be produced later.
 *
 * <p>The actual PDF bytes are NOT stored in the database – they are generated
 * on-the-fly from this entity's data each time a download is requested.
 *
 * <p>Annotations used:
 * <ul>
 *   <li>{@code @Entity} – marks the class as a JPA-managed persistent object.</li>
 *   <li>{@code @Table} – maps the entity to the "invoices" table in the DB.</li>
 *   <li>{@code @Id} / {@code @GeneratedValue} – defines a surrogate primary key
 *       auto-generated by the database sequence.</li>
 * </ul>
 */
@Entity
@Table(name = "invoices")
public class Invoice {

    /**
     * Surrogate primary key.
     * IDENTITY strategy delegates sequence generation to the database engine.
     */
    @Id
    @GeneratedValue(strategy = GenerationType.IDENTITY)
    private Long id;

    /**
     * Human-readable invoice number, e.g. "INV-2024-001".
     * Must be unique so that each generated PDF has a distinct identifier.
     */
    @Column(name = "invoice_number", nullable = false, unique = true, length = 50)
    private String invoiceNumber;

    /**
     * Name of the customer the invoice is billed to.
     */
    @Column(name = "customer_name", nullable = false, length = 200)
    private String customerName;

    /**
     * Email address of the customer, included in the PDF header.
     */
    @Column(name = "customer_email", nullable = false, length = 200)
    private String customerEmail;

    /**
     * Total amount due, stored as a NUMERIC(12,2) column to avoid floating-point issues.
     */
    @Column(name = "total_amount", nullable = false, precision = 12, scale = 2)
    private BigDecimal totalAmount;

    /**
     * ISO currency code, e.g. "USD", "EUR". Defaults to "USD".
     */
    @Column(name = "currency", nullable = false, length = 10)
    private String currency = "USD";

    /**
     * The date the invoice was issued (stored as a SQL DATE column).
     */
    @Column(name = "issue_date", nullable = false)
    private LocalDate issueDate;

    /**
     * Optional free-text notes printed at the bottom of the invoice PDF.
     */
    @Column(name = "notes", length = 1000)
    private String notes;

    // ── Constructors ──────────────────────────────────────────────────────────

    /** Default constructor required by JPA. */
    protected Invoice() {
    }

    /**
     * Full constructor for creating a new invoice programmatically.
     *
     * @param invoiceNumber  unique human-readable identifier (e.g. "INV-2024-001")
     * @param customerName   billing customer name
     * @param customerEmail  billing customer email
     * @param totalAmount    total amount due
     * @param currency       ISO currency code (e.g. "USD")
     * @param issueDate      date the invoice was issued
     * @param notes          optional footer notes (may be null)
     */
    public Invoice(String invoiceNumber, String customerName, String customerEmail,
                   BigDecimal totalAmount, String currency, LocalDate issueDate, String notes) {
        this.invoiceNumber = invoiceNumber;
        this.customerName = customerName;
        this.customerEmail = customerEmail;
        this.totalAmount = totalAmount;
        this.currency = currency;
        this.issueDate = issueDate;
        this.notes = notes;
    }

    // ── Getters and setters ───────────────────────────────────────────────────

    public Long getId() { return id; }

    public String getInvoiceNumber() { return invoiceNumber; }
    public void setInvoiceNumber(String invoiceNumber) { this.invoiceNumber = invoiceNumber; }

    public String getCustomerName() { return customerName; }
    public void setCustomerName(String customerName) { this.customerName = customerName; }

    public String getCustomerEmail() { return customerEmail; }
    public void setCustomerEmail(String customerEmail) { this.customerEmail = customerEmail; }

    public BigDecimal getTotalAmount() { return totalAmount; }
    public void setTotalAmount(BigDecimal totalAmount) { this.totalAmount = totalAmount; }

    public String getCurrency() { return currency; }
    public void setCurrency(String currency) { this.currency = currency; }

    public LocalDate getIssueDate() { return issueDate; }
    public void setIssueDate(LocalDate issueDate) { this.issueDate = issueDate; }

    public String getNotes() { return notes; }
    public void setNotes(String notes) { this.notes = notes; }
}
