package com.example.kafkaconsumer.domain;

import java.math.BigDecimal;
import java.time.Instant;

/**
 * Domain object representing an order event received from Kafka.
 *
 * <p>This record is the value type that the {@code @KafkaListener} deserialises
 * from the incoming Kafka message JSON payload. The field names must match the
 * JSON property names produced by the Kafka producer so Jackson can reconstruct
 * the object correctly.
 *
 * <p>Using a Java {@code record} ensures immutability: once an
 * {@code OrderEvent} is created its fields cannot change. This is appropriate
 * for event-sourcing patterns where received events should never be mutated
 * after they are consumed.
 *
 * <h2>How deserialisation works</h2>
 * <p>Spring Kafka's {@code JsonDeserializer} uses Jackson to convert the
 * raw JSON bytes from the Kafka message value into an {@code OrderEvent}
 * instance. The deserialiser needs to know the target Java type; this is
 * configured via {@code spring.kafka.consumer.value-deserializer} and
 * {@code spring.kafka.consumer.properties.spring.json.value.default.type}
 * in {@code application.yml}.
 *
 * @param eventId      unique identifier for this specific event (UUID v4)
 * @param orderId      identifier of the order that triggered the event
 * @param customerId   identifier of the customer who placed the order
 * @param product      name or SKU of the product being ordered
 * @param quantity     number of units ordered
 * @param totalAmount  total monetary value of the order
 * @param status       current status of the order (e.g. CREATED, CANCELLED)
 * @param occurredAt   UTC timestamp when the event was generated by the producer
 */
public record OrderEvent(
        String eventId,
        String orderId,
        String customerId,
        String product,
        int quantity,
        BigDecimal totalAmount,
        OrderStatus status,
        Instant occurredAt
) {}
