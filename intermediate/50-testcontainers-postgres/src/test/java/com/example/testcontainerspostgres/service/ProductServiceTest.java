package com.example.testcontainerspostgres.service;

import com.example.testcontainerspostgres.dto.ProductRequest;
import com.example.testcontainerspostgres.dto.ProductResponse;
import com.example.testcontainerspostgres.entity.Product;
import com.example.testcontainerspostgres.exception.ProductNotFoundException;
import com.example.testcontainerspostgres.repository.ProductRepository;
import org.junit.jupiter.api.BeforeEach;
import org.junit.jupiter.api.DisplayName;
import org.junit.jupiter.api.Test;
import org.junit.jupiter.api.extension.ExtendWith;
import org.mockito.InjectMocks;
import org.mockito.Mock;
import org.mockito.junit.jupiter.MockitoExtension;

import java.math.BigDecimal;
import java.util.List;
import java.util.Optional;

import static org.assertj.core.api.Assertions.assertThat;
import static org.assertj.core.api.Assertions.assertThatThrownBy;
import static org.mockito.ArgumentMatchers.any;
import static org.mockito.BDDMockito.given;
import static org.mockito.BDDMockito.then;
import static org.mockito.Mockito.never;

/**
 * Unit tests for {@link ProductService}.
 *
 * <p><b>What is a unit test?</b>
 * A unit test exercises a single class (the "unit") in complete isolation
 * from its dependencies. Here, the dependency is {@link ProductRepository}.
 * We replace it with a Mockito mock so:
 * <ul>
 *   <li>No database is needed — no Docker, no H2, no network.</li>
 *   <li>Tests run in milliseconds.</li>
 *   <li>We can simulate any scenario (empty result, exception, etc.) precisely.</li>
 * </ul>
 *
 * <p><b>Key annotations:</b>
 * <ul>
 *   <li>{@code @ExtendWith(MockitoExtension.class)} — integrates Mockito's lifecycle
 *       with JUnit 5. It automatically initialises all {@code @Mock} fields before
 *       each test and verifies them after (no {@code MockitoAnnotations.openMocks}
 *       needed).</li>
 *   <li>{@code @Mock} — creates a Mockito mock for the annotated field. The mock
 *       does nothing by default; we configure its behaviour with {@code given(...)}.</li>
 *   <li>{@code @InjectMocks} — creates a real instance of {@code ProductService} and
 *       injects the mock {@code ProductRepository} into it via constructor injection.</li>
 * </ul>
 *
 * <p><b>BDD-style given/when/then:</b>
 * Each test follows the Arrange-Act-Assert (or BDD's given-when-then) pattern to keep
 * tests readable and structured.
 *
 * <p><b>What is NOT tested here:</b>
 * The actual SQL queries and database behaviour are NOT tested here. That is the
 * responsibility of the integration tests in {@code ProductRepositoryIntegrationTest},
 * which use a real PostgreSQL container via Testcontainers.
 */
@ExtendWith(MockitoExtension.class)
class ProductServiceTest {

    /** Mockito mock — replaced by a fake object; no real database calls. */
    @Mock
    private ProductRepository productRepository;

    /**
     * Real instance of {@link ProductService}; Mockito injects the mock
     * {@code productRepository} via constructor injection.
     */
    @InjectMocks
    private ProductService productService;

    // Shared test fixtures rebuilt before each test to guarantee isolation
    private Product laptopProduct;
    private Product phoneProduct;

    /**
     * Builds a consistent set of test fixtures before each test.
     *
     * <p>We use reflection to inject the {@code id} field because the
     * {@link Product} entity does not expose a public setter for it —
     * the ID is generated by the database in production. In unit tests it is
     * acceptable to fake the repository's return value this way.
     */
    @BeforeEach
    void setUp() throws Exception {
        laptopProduct = new Product("Laptop Pro", "High-performance laptop", new BigDecimal("999.99"), 10);
        setId(laptopProduct, 1L);

        phoneProduct = new Product("Smartphone X", "Latest smartphone", new BigDecimal("499.99"), 25);
        setId(phoneProduct, 2L);
    }

    /**
     * Helper that injects a value into the private {@code id} field of a
     * {@link Product} via reflection — simulating what the database would do
     * after an INSERT.
     */
    private void setId(Product product, Long id) throws Exception {
        var idField = Product.class.getDeclaredField("id");
        idField.setAccessible(true);
        idField.set(product, id);
    }

    // =========================================================================
    // findAll
    // =========================================================================

    @Test
    @DisplayName("findAll returns a list of ProductResponse DTOs mapped from all entities")
    void findAll_returnsAllProductsAsDtos() {
        // given — repository returns two products
        given(productRepository.findAll()).willReturn(List.of(laptopProduct, phoneProduct));

        // when
        List<ProductResponse> result = productService.findAll();

        // then
        assertThat(result).hasSize(2);

        ProductResponse first = result.get(0);
        assertThat(first.id()).isEqualTo(1L);
        assertThat(first.name()).isEqualTo("Laptop Pro");
        assertThat(first.price()).isEqualByComparingTo("999.99");
        assertThat(first.stockQuantity()).isEqualTo(10);

        ProductResponse second = result.get(1);
        assertThat(second.id()).isEqualTo(2L);
        assertThat(second.name()).isEqualTo("Smartphone X");
    }

    @Test
    @DisplayName("findAll returns an empty list when no products exist")
    void findAll_returnsEmptyList_whenNoProducts() {
        // given — repository has no data
        given(productRepository.findAll()).willReturn(List.of());

        // when
        List<ProductResponse> result = productService.findAll();

        // then
        assertThat(result).isEmpty();
    }

    // =========================================================================
    // findById
    // =========================================================================

    @Test
    @DisplayName("findById returns the correct ProductResponse DTO when the product exists")
    void findById_returnsDto_whenProductExists() {
        // given
        given(productRepository.findById(1L)).willReturn(Optional.of(laptopProduct));

        // when
        ProductResponse result = productService.findById(1L);

        // then
        assertThat(result.id()).isEqualTo(1L);
        assertThat(result.name()).isEqualTo("Laptop Pro");
        assertThat(result.description()).isEqualTo("High-performance laptop");
        assertThat(result.price()).isEqualByComparingTo("999.99");
        assertThat(result.stockQuantity()).isEqualTo(10);
    }

    @Test
    @DisplayName("findById throws ProductNotFoundException when the product does not exist")
    void findById_throwsNotFoundException_whenProductNotFound() {
        // given — simulate a missing product
        given(productRepository.findById(99L)).willReturn(Optional.empty());

        // when / then — assertThatThrownBy checks both the exception type and message
        assertThatThrownBy(() -> productService.findById(99L))
                .isInstanceOf(ProductNotFoundException.class)
                .hasMessageContaining("99");
    }

    // =========================================================================
    // search
    // =========================================================================

    @Test
    @DisplayName("search returns products whose name contains the keyword")
    void search_returnsMatchingProducts() {
        // given — repository returns laptop when searching "lap"
        given(productRepository.findByNameContainingIgnoreCase("lap"))
                .willReturn(List.of(laptopProduct));

        // when
        List<ProductResponse> result = productService.search("lap");

        // then
        assertThat(result).hasSize(1);
        assertThat(result.get(0).name()).isEqualTo("Laptop Pro");
    }

    @Test
    @DisplayName("search returns empty list when no products match the keyword")
    void search_returnsEmptyList_whenNoMatch() {
        // given — no products match "xyz"
        given(productRepository.findByNameContainingIgnoreCase("xyz"))
                .willReturn(List.of());

        // when
        List<ProductResponse> result = productService.search("xyz");

        // then
        assertThat(result).isEmpty();
    }

    // =========================================================================
    // findInStock
    // =========================================================================

    @Test
    @DisplayName("findInStock returns only products with stock quantity greater than zero")
    void findInStock_returnsInStockProducts() {
        // given — both sample products have stock > 0
        given(productRepository.findAllInStock()).willReturn(List.of(laptopProduct, phoneProduct));

        // when
        List<ProductResponse> result = productService.findInStock();

        // then
        assertThat(result).hasSize(2);
        assertThat(result).allSatisfy(p -> assertThat(p.stockQuantity()).isGreaterThan(0));
    }

    // =========================================================================
    // findByPriceRange
    // =========================================================================

    @Test
    @DisplayName("findByPriceRange returns products within the given price bounds")
    void findByPriceRange_returnsProductsInRange() {
        BigDecimal min = new BigDecimal("400.00");
        BigDecimal max = new BigDecimal("600.00");

        // given — only the phone is in the 400-600 range
        given(productRepository.findByPriceBetween(min, max)).willReturn(List.of(phoneProduct));

        // when
        List<ProductResponse> result = productService.findByPriceRange(min, max);

        // then
        assertThat(result).hasSize(1);
        assertThat(result.get(0).name()).isEqualTo("Smartphone X");
        assertThat(result.get(0).price()).isEqualByComparingTo("499.99");
    }

    // =========================================================================
    // create
    // =========================================================================

    @Test
    @DisplayName("create saves a new product and returns the response DTO with the generated ID")
    void create_savesProductAndReturnsDto() {
        // given — the request payload sent by the client
        ProductRequest request = new ProductRequest(
                "Tablet Z", "A great tablet", new BigDecimal("299.99"), 50);

        // Simulate the repository returning the entity after a successful INSERT
        // (the entity now has an ID assigned by the database)
        given(productRepository.save(any(Product.class))).willReturn(laptopProduct);

        // when
        ProductResponse result = productService.create(request);

        // then — save() must have been called exactly once
        then(productRepository).should().save(any(Product.class));
        // The returned DTO carries the ID set by the repository (simulated as laptopProduct.id)
        assertThat(result.id()).isEqualTo(1L);
    }

    @Test
    @DisplayName("create maps all request fields correctly to the saved entity")
    void create_mapsRequestFieldsCorrectly() throws Exception {
        // given — build a product that mirrors what we expect save() to return
        Product savedProduct = new Product(
                "Wireless Keyboard", "Ergonomic keyboard", new BigDecimal("79.99"), 100);
        setId(savedProduct, 5L);

        ProductRequest request = new ProductRequest(
                "Wireless Keyboard", "Ergonomic keyboard", new BigDecimal("79.99"), 100);

        given(productRepository.save(any(Product.class))).willReturn(savedProduct);

        // when
        ProductResponse result = productService.create(request);

        // then — all fields should be preserved from the request → entity → DTO pipeline
        assertThat(result.name()).isEqualTo("Wireless Keyboard");
        assertThat(result.description()).isEqualTo("Ergonomic keyboard");
        assertThat(result.price()).isEqualByComparingTo("79.99");
        assertThat(result.stockQuantity()).isEqualTo(100);
    }

    // =========================================================================
    // update
    // =========================================================================

    @Test
    @DisplayName("update modifies existing product fields and returns the updated DTO")
    void update_modifiesProductAndReturnsUpdatedDto() throws Exception {
        // given — an updated product to return from save()
        Product updatedProduct = new Product(
                "Laptop Pro Max", "Upgraded laptop", new BigDecimal("1199.99"), 5);
        setId(updatedProduct, 1L);

        given(productRepository.findById(1L)).willReturn(Optional.of(laptopProduct));
        given(productRepository.save(any(Product.class))).willReturn(updatedProduct);

        ProductRequest updateRequest = new ProductRequest(
                "Laptop Pro Max", "Upgraded laptop", new BigDecimal("1199.99"), 5);

        // when
        ProductResponse result = productService.update(1L, updateRequest);

        // then
        assertThat(result.id()).isEqualTo(1L);
        assertThat(result.name()).isEqualTo("Laptop Pro Max");
        assertThat(result.price()).isEqualByComparingTo("1199.99");
        assertThat(result.stockQuantity()).isEqualTo(5);
    }

    @Test
    @DisplayName("update throws ProductNotFoundException when product does not exist")
    void update_throwsNotFoundException_whenProductNotFound() {
        // given — product 99 does not exist
        given(productRepository.findById(99L)).willReturn(Optional.empty());
        ProductRequest request = new ProductRequest("Name", "Desc", BigDecimal.TEN, 1);

        // when / then
        assertThatThrownBy(() -> productService.update(99L, request))
                .isInstanceOf(ProductNotFoundException.class)
                .hasMessageContaining("99");

        // save() must never be called when the entity was not found
        then(productRepository).should(never()).save(any());
    }

    // =========================================================================
    // delete
    // =========================================================================

    @Test
    @DisplayName("delete calls deleteById when the product exists")
    void delete_callsDeleteById_whenProductExists() {
        // given — product 1 exists
        given(productRepository.existsById(1L)).willReturn(true);

        // when
        productService.delete(1L);

        // then — deleteById must be called exactly once with the correct ID
        then(productRepository).should().deleteById(1L);
    }

    @Test
    @DisplayName("delete throws ProductNotFoundException and never calls deleteById when product does not exist")
    void delete_throwsNotFoundException_whenProductNotFound() {
        // given — product 99 does not exist
        given(productRepository.existsById(99L)).willReturn(false);

        // when / then
        assertThatThrownBy(() -> productService.delete(99L))
                .isInstanceOf(ProductNotFoundException.class)
                .hasMessageContaining("99");

        // deleteById must never be called when the entity does not exist
        then(productRepository).should(never()).deleteById(any());
    }
}
